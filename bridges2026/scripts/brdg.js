function redux() {
  let rdx = {};

  let F = 2;

  rdx['dx'] = {};
  for (let key in info.dx) {
    rdx.dx[key] = Math.floor(info.dx[key]/F);
  }

  for (let key in info) {
    if (key == 'dx') { continue; }
    if (key == '') { continue; }

    let ch = info[key];
    let dst_ch = [];

    for (let i=0; i<ch.length; i++) {
      let src_p = ch[i][0];
      let src_abg = ch[i][1];

      let dst_p = [
        (src_p[0]%2) ? (Math.floor((src_p[0]+1)/F)-1) : Math.floor(src_p[0]/F),
        (src_p[1]%2) ? (Math.floor((src_p[1]+1)/F)-1) : Math.floor(src_p[1]/F),
        (src_p[2]%2) ? (Math.floor((src_p[2]+1)/F)-1) : Math.floor(src_p[2]/F),
      ];

      let dst_abg = [ [], [], [] ];
      dst_abg[0] = [
        (src_abg[0][0]%2) ? (Math.floor((src_abg[0][0]+1)/F)-1) : Math.floor(src_abg[0][0]/F),
        (src_abg[0][1]%2) ? (Math.floor((src_abg[0][1]+1)/F)-1) : Math.floor(src_abg[0][1]/F),
        (src_abg[0][2]%2) ? (Math.floor((src_abg[0][2]+1)/F)-1) : Math.floor(src_abg[0][2]/F),
      ];

      dst_abg[1] = [
        (src_abg[1][0]%2) ? (Math.floor((src_abg[1][0]+1)/F)-1) : Math.floor(src_abg[1][0]/F),
        (src_abg[1][1]%2) ? (Math.floor((src_abg[1][1]+1)/F)-1) : Math.floor(src_abg[1][1]/F),
        (src_abg[1][2]%2) ? (Math.floor((src_abg[1][2]+1)/F)-1) : Math.floor(src_abg[1][2]/F),
      ];

      dst_abg[2] = [
        (src_abg[2][0]%2) ? (Math.floor((src_abg[2][0]+1)/F)-1) : Math.floor(src_abg[2][0]/F),
        (src_abg[2][1]%2) ? (Math.floor((src_abg[2][1]+1)/F)-1) : Math.floor(src_abg[2][1]/F),
        (src_abg[2][2]%2) ? (Math.floor((src_abg[2][2]+1)/F)-1) : Math.floor(src_abg[2][2]/F),
      ];

      dst_ch.push( [dst_p, dst_abg] );

    }

    rdx[key] = dst_ch;
  }

  return rdx;
}

